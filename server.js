// Fun√ß√£o para processar resposta da IA
function processAIResponse(originalSuggestions, aiResponse) {
  console.log('ü§ñ [AI-PROCESSING] Processando resposta da IA...');
  
  try {
    // Limpar resposta (remover markdown, etc.)
    const cleanResponse = aiResponse.replace(/```json\n?|\n?```/g, '').trim();
    
    // Tentar parsear JSON
    const parsed = JSON.parse(cleanResponse);
    console.log('‚úÖ [AI-PROCESSING] JSON v√°lido parseado');
    
    if (parsed.suggestions && Array.isArray(parsed.suggestions)) {
      console.log(`üîÑ [AI-PROCESSING] ${parsed.suggestions.length} sugest√µes processadas pela IA`);
      return parsed.suggestions;
    }
    
    throw new Error('Formato de resposta inv√°lido');
    
  } catch (error) {
    console.error('‚ùå [AI-PROCESSING] Erro ao processar resposta:', error.message);
    console.log('üîÑ [AI-PROCESSING] Usando fallback estruturado...');
    
    // Fallback: estruturar sugest√µes originais
    return originalSuggestions.map(suggestion => ({
      blocks: {
        problem: `‚ö†Ô∏è ${suggestion.message || suggestion.title || 'Problema detectado'}`,
        cause: 'üéØ An√°lise autom√°tica identificou desvio dos padr√µes t√©cnicos',
        solution: `üõ†Ô∏è ${suggestion.action || suggestion.description || 'Ajuste recomendado'}`,
        tip: 'üí° Monitore resultado em diferentes sistemas de reprodu√ß√£o'
      },
      metadata: {
        priority: suggestion.priority || 'm√©dia',
        difficulty: 'intermedi√°rio', 
        confidence: suggestion.confidence || 0.7,
        frequency_range: suggestion.frequency_range || 'amplo espectro',
        processing_type: 'Ajuste geral',
        expected_improvement: 'Melhoria na qualidade sonora geral'
      },
      aiEnhanced: false
    }));
  }
}

