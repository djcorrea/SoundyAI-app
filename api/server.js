const fileFilter = (req, file, cb) => {
  const allowedTypes = [
    "audio/mpeg",   // mp3
    "audio/wav",    // wav
    "audio/x-wav",  // outra varia√ß√£o wav
    "audio/wave",   // outra varia√ß√£o wav
    "audio/x-m4a",  // m4a
    "audio/mp4",    // m4a/mp4
  ];
  if (!allowedTypes.includes(file.mimetype)) {
    return cb(
      new Error("Tipo de arquivo n√£o permitido. Envie apenas mp3, wav ou m4a."),
      false
    );
  }
  cb(null, true);
};

